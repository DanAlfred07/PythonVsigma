# Arthmetic Operations in Numpy

# Addtion
import numpy as np
x = np.array([1,2,3,4])
y = np.array([1,2,3,4])

result = x + y

print("Result: ",result)

# Substraction

x = np.array([1,2,3,4])
y = np.array([1,2,3,4])

result = x - y

print("Result: ",result)

# Division

x = np.array([1,2,3,4])
y = np.array([1,2,3,4])

result = x / y

print("Result: ",result)

# Module Divsion

x = np.array([1,2,3,4])
y = np.array([1,2,3,4])

result = x % y

print("Result: ",result)

# Reciprocal

x = np.array([1,2,3,4])

varadd = np.reciprocal(x)

print(varadd)

# Two Dimensional Arthmetic in Numpy Arrays

x = np.array([[1,2,3,4],[1,2,3,4]])
y = np.array([[1,2,3,4],[1,2,3,4]])

result = x + y
print("Result: ",result)